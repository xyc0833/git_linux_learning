# ---------- 1. 变量 ----------
NAME="Shell 学习者"
DATE=$(date +"%Y-%m-%d")

echo "欢迎你，$NAME"
echo "今天的日期是：$DATE"
echo "--------------------------------"

# ---------- 2. 用户输入 ----------
read -p "请输入你的年龄: " AGE

# ---------- 3. 条件判断 ----------
if [ "$AGE" -lt 18 ]; then
    echo "你还未成年"
elif [ "$AGE" -ge 18 ] && [ "$AGE" -lt 60 ]; then
    echo "你是成年人"
else
    echo "你已进入退休年龄"
fi

echo "--------------------------------"

# ---------- 4. for 循环 ----------
echo "for 循环演示："
for i in 1 2 3 4 5
do
    echo "当前数字是：$i"
done

echo "--------------------------------"

# ---------- 5. while 循环 ----------
COUNT=1
echo "while 循环演示："
while [ $COUNT -le 3 ]
do
    echo "COUNT = $COUNT"
    COUNT=$((COUNT + 1))
done

echo "--------------------------------"

# ---------- 6. 函数 ----------
say_hello() {
    echo "你好，$1！欢迎学习 Shell 脚本"
}

say_hello "$NAME"

echo "--------------------------------"

# ---------- 7. 脚本结束 ----------
echo "脚本执行完毕，再见！"
